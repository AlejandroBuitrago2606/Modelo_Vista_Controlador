package Model;

public class Token {

    int id;
    int NumTok;
    String Nameprog;
    String City;
    String Site;
    int idAppren;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getNumTok() {
        return NumTok;
    }

    public void setNumTok(int NumTok) {
        this.NumTok = NumTok;
    }

    public String getNameprog() {
        return Nameprog;
    }

    public void setNameprog(String Nameprog) {
        this.Nameprog = Nameprog;
    }

    public String getCity() {
        return City;
    }

    public void setCity(String City) {
        this.City = City;
    }

    public String getSite() {
        return Site;
    }

    public void setSite(String Site) {
        this.Site = Site;
    }

    public int getIdAppren() {
        return idAppren;
    }

    public void setIdAppren(int idAppren) {
        this.idAppren = idAppren;
    }

}
